function mayus(asd){
    return asd.toUpperCase();

}

console.log(mayus("asdasdasdasdasd"));


function tipo(asd){
    return typeof(asd);
}

console.log(tipo(4));

function edadPerros(numero){
    return numero * 7;
}

console.log(edadPerros(3));

